------------------------------------------------------------

CREATE TABLE MEMBER_TEST (

    -------------------------------------------------
    -- 회원 기본 정보
    -------------------------------------------------

    MEM_ID          VARCHAR2(15) PRIMARY KEY,
    MEM_PASS        VARCHAR2(100) NOT NULL,
    MEM_NAME        VARCHAR2(20) NOT NULL,

    -------------------------------------------------
    -- 회원 상세 정보
    -------------------------------------------------

    MEM_ADD         VARCHAR2(100),
    MEM_MAIL        VARCHAR2(50),

    -------------------------------------------------
    -- 가입일
    -------------------------------------------------

    MEM_CREATED     DATE DEFAULT SYSDATE

);

